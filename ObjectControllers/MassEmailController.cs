        public static List<EmailQueueItem> GetQueueItemsToProcess(int batchId)
        {
            List<EmailQueueItem> queue = new List<EmailQueueItem>();
            DataTable dt = null;


            List<SqlParameter> sqlParams = new List<SqlParameter>();
            sqlParams.Add(new SqlParameter("BatchId", batchId));
            var records = DataAccess.ExecuteStoredProcedure("usp_GetEmailQueueToProcess", sqlParams, "RecordsToProcess");

            if (records.Tables.Count < 1)
            {
                dt = records.Tables[0];
                if (dt.Rows.Count < 1)
                    return null;
            }

            dt = records.Tables[0];
            foreach (DataRow row in dt.Rows)
            {
                EmailQueueItem item = new EmailQueueItem();
                item.EmailQueueId = row["EmailQueueId"].GetDataInt();
                item.BatchId = row["BatchId"].GetDataInt();
                item.TemplateId = row["TemplateId"].GetDataInt();
                item.NPI = row["NPI"].GetDataString();
                item.TaxId = row["TaxId"].GetDataString();
                item.RegId = row["Reg_Id"].GetDataInt();
                item.Name = row["Name"].GetDataString();
                item.EmailAddresses = row["EmailAddress"].GetDataString();
                item.IsSent = row["IsSent"].GetDataBool();
                item.TemplateBody = row["TemplateBody"].GetDataString();
                item.ParsedEmailBody = row["ParsedEmailBody"].GetDataString();
                item.EmailFields = row["EmailFields"].GetDataString();
                item.EmailValues = row["EmailValues"].GetDataString();
                item.CreateDateTime = row["CreateDateTime"].GetDataDateTime().Value;
                item.EmailSubject = row["EmailSubject"].GetDataString();

                queue.Add(item);
            }
            return queue;
        }

