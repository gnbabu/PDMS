        public static int BatchJobCreate(int recordCount, int templateId, string subject)
        {
            int jobId = 0;
            try
            {
                List<SqlParameter> sqlParams = new List<SqlParameter>();
                sqlParams.Add(new SqlParameter("TotalRecords", recordCount));
                sqlParams.Add(new SqlParameter("TemplateId", templateId));
                sqlParams.Add(new SqlParameter("EmailSubject", subject));

                jobId = Convert.ToInt32(DataAccess.ExecuteScalar("usp_EmailJobCreate", sqlParams, CommandType.StoredProcedure));
            }
            catch (Exception ex)
            {
                throw CoreException.ThrowException(ex);
            }

            return jobId;
        }

        public static List<EmailBatch> EmailBatchJobsToProcess()
        {
            List<EmailBatch> toProcess = new List<EmailBatch>();
            try
            {
                DataTable dt = null;
                var records = DataAccess.ExecuteStoredProcedure("usp_EmailBatchJobsToProcess", "RecordsToProcess");

                if (records.Tables.Count < 1)
                {
                    dt = records.Tables[0];
                    if (dt.Rows.Count < 1)
                        return toProcess;
                }
                dt = records.Tables[0];
                foreach (DataRow row in dt.Rows)
                {
                    EmailBatch batch = new EmailBatch();
                    batch.EmailBatchId = row["EmailBatchId"].GetDataInt();
                    batch.EmailSubject = row["EmailSubject"].GetDataString();
                    batch.TemplateId = row["TemplateId"].GetDataInt();
                    batch.CreateDateTime = row["CreateDateTime"].GetDataDateTime().Value;
                    batch.JobComplete = row["JobComplete"].GetDataBool();

                    toProcess.Add(batch);
                }

                return toProcess;
            }
            catch (Exception ex)
            {
                throw CoreException.ThrowException(ex);
            }
        }

        public static List<EmailBatch> GetAllBatchJobs()
        {
            List<EmailBatch> BatchJobs = new List<EmailBatch>();
            try
            {
                DataTable dt = null;
                var records = DataAccess.ExecuteStoredProcedure("usp_GetEmailBatchJobs", "BatchJobList");

                if (records.Tables.Count < 1)
                {
                    dt = records.Tables[0];
                    if (dt.Rows.Count < 1)
                        return BatchJobs;
                }
                dt = records.Tables[0];
                foreach (DataRow row in dt.Rows)
                {
                    EmailBatch batch = new EmailBatch();
                    batch.EmailBatchId = row["EmailBatchId"].GetDataInt();
                    batch.EmailSubject = row["EmailSubject"].GetDataString();
                    batch.TotalRecords = row["TotalRecords"].GetDataInt();
                    batch.TemplateId = row["TemplateId"].GetDataInt();
                    batch.CreateDateTime = row["CreateDateTime"].GetDataDateTime().Value;
                    batch.JobComplete = row["JobComplete"].GetDataBool();

                    if (row["EmailSendDateTime"] != DBNull.Value && row["EmailSendDateTime"] != null)
                    {
                        batch.EmailSendDateTime = row["EmailSendDateTime"].GetDataDateTime().Value;
                    }

                    


                    BatchJobs.Add(batch);
                }

                return BatchJobs;
            }
            catch (Exception ex)
            {
                throw CoreException.ThrowException(ex);
            }

        }



