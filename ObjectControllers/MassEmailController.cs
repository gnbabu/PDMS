        public static EmailTemplate GetTemplateByName(string templateName)
        {
            EmailTemplate template = null;
            try
            {
                List<SqlParameter> sqlParams = new List<SqlParameter>();
                sqlParams.Add(new SqlParameter("TemplateName", templateName));

                DataSet ds = new DataSet();
                ds = DataAccess.ExecuteStoredProcedure("usp_EmailTemplateByName", sqlParams, "Templates");
                if (ds.Tables.Count > 0)
                    throw new Exception("Template " +templateName + " not found");

                var dt = ds.Tables[0];
                if (dt.Rows.Count > 0)
                {
                    DataRow dr = dt.Rows[0];
                    template = new EmailTemplate()
                    {
                        TemplateId = Convert.ToInt32(dr["TemplateId"]),
                        TemplateName = Convert.ToString(dr["TemplateName"]),
                        IsActive = Convert.ToBoolean(dr["IsActive"]),
                        TemplateNotes = Convert.ToString(dr["TemplateNotes"]),
                        BodyTemplateName = Methods.GetStringValue(dr, "BodyTemplateName"),
                        CreateDateTime = Convert.ToDateTime(dr["CreateDateTime"]),
                        LastModifiedUser = Convert.ToString(dr["LastModifiedUser"])
                    };
                }
                return template;
            }
            catch (Exception ex)
            {
                throw CoreException.ThrowException(ex);
            }
        }

